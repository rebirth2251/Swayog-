document.addEventListener("DOMContentLoaded", function() {
    const runningProgress = document.getElementById("running-progress");
    const runningRange = document.getElementById("running-range");
  
    // Function to update progress bar width
    function updateProgressBarWidth(progressBar, range) {
      progressBar.style.width = range.value + "%";
    }
  
    // Event listener for range input change
    runningRange.addEventListener("input", function() {
      updateProgressBarWidth(runningProgress, runningRange);
    });
  
    // Initialize progress bar width
    updateProgressBarWidth(runningProgress, runningRange);
  
  });

